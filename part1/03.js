/* Rules for Naming Variables */

// you cannot start with number 
// example:
// 1value (invalid)
// value1 (valid)

var value1 = 2;
console.log(value1 ** 3);
// here '**' can be used to find 'power' of a numerical value

// you can use only undersore _ or dollar symbol 
// first_name (valid)
// _firstname (valid) 

// first$name (valid)
// $firstname (valid)

// you cannot use spaces 
// var first_name = "harshit"; // snake case writing 
// var firstName = "harshit"; // camel case writing 
// first name (invalid)

// JS convention:
// start the variable name with small letter and use camelCase